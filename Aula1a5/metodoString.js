let nome = 'Nicollas Pereira'
console.log(nome[16]);
console.log(nome.length); // Retorna a quantidade de caracteres de presentes na String

console.log(nome.charAt(16)); //CharAT acessa determinado caractere baseado no índice escolhido

console.log(nome.toLowerCase()); // Transforma todos os caracteres em letras minusculas

console.log(nome.toUpperCase()); // Trasforma os caracteres em letras maiusculas

console.log(nome.substring(0, 8)); // Corta a String a partir do primeiro índice até o segundo índice

console.log(nome.split(' ')); // Split separa a string de acordo com o que você especificou